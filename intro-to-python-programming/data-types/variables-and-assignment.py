# Variables - names used to identify values
# They are called identifiers, a more general term that
# applies to variables, constants, function names, class names and more

pi = 3.14
life_the_universe_and_everything = 42

# the syntax used when creating variables e.g. `foo = 'abc` is called assignment
# We can say that the variable foo is assigned to the value `abc` or 
# that the value 'abc' is assigned to the variable `foo`

foo = "Hello, world." # assingment or initialization
foo = "That's all folks" # assingment


