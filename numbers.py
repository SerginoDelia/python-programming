x = 10

# Numbers in binary format (prefix 0b)
y = 0b10
print(y)

# print the binary representation of a number
print(bin(x))

# we can also set numbers in hexadecimal format (prefix: 0x)
z = 0x12c
print(z)

# print number in hexadecimal format
print(hex(x))

# python also have support for complex numbers
# a + bi

a = 1 + 2j  # j is an imaginary number in math
print(a)
