x = 10 + 3
x = 10 - 3
x = 10 * 3

# / return a floating point number
x = 10 / 3
print(x)

# // returns an integer
x = 10 // 3
print(x)

# modulo - the remainder of a division
x = 10 % 3
print(x)

# exponents - left to the power of right
x = 10 ** 3
print(x)

# augmented assignment operator (the left side can be any of the operator above)
x += 1
print(x)

